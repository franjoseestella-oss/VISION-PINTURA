export function getLanguage(): Language {
    return (localStorage.getItem('language') as Language) || 'es';
}

export function setLanguage(lang: Language) {
    localStorage.setItem('language', lang);
    window.dispatchEvent(new Event('languageChange'));
}

export function useTranslation() {
    const [language, setLangState] = useState<Language>(getLanguage());

    useEffect(() => {
        const handleLangChange = () => setLangState(getLanguage());
        window.addEventListener('languageChange', handleLangChange);
        return () => window.removeEventListener('languageChange', handleLangChange);
    }, []);

    const t = (key: string): string => {
        return TRANSLATIONS[language]?.[key] || key;
    };

    const setLanguageGlobal = (lang: Language) => {
        setLanguage(lang);
    };

    return { t, language, setLanguage: setLanguageGlobal };
}
